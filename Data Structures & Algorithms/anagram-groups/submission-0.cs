public class Solution {
    public List<List<string>> GroupAnagrams(string[] strs) {
            for(int i = 0; i < strs.Length; i++){
                for (int j=0; j < strs[i].Length; j++){
                    if(strs[i][j] == 'a'){
                        console.WriteLine("Nice");
                    }
                }

            } 
        
    }
}
